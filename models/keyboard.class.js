/**
 * Represents the keyboard input for the game.
 * This class tracks the state of specific keys used for player controls.
 */
class Keyboard {
  /**
   * Indicates whether the left arrow key is pressed.
   * @type {boolean}
   */
  LEFT = false;

  /**
   * Indicates whether the right arrow key is pressed.
   * @type {boolean}
   */
  RIGHT = false;

  /**
   * Indicates whether the up arrow key is pressed.
   * @type {boolean}
   */
  UP = false;

  /**
   * Indicates whether the down arrow key is pressed.
   * @type {boolean}
   */
  DOWN = false;

  /**
   * Indicates whether the spacebar is pressed.
   * @type {boolean}
   */
  SPACE = false;

  /**
   * Indicates whether the 'D' key is pressed.
   * @type {boolean}
   */
  D = false;
}